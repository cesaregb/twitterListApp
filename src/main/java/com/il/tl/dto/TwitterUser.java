public class TwitterUser {
	private double id; 
	private String name;
	private String location; 
	private String profile_image_url; 
	private double followers_count;
	private double friends_count;
	private String lang;
	private String description;
	private String time_zone;
	private String statuses_count;
	private String screen_name;
	/**
	 * @return the id
	 */
	public double getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(double id) {
		this.id = id;
	}
	/**
	 * @return the name
	 */
	public String getName() {
		return name;
	}
	/**
	 * @param name the name to set
	 */
	public void setName(String name) {
		this.name = name;
	}
	/**
	 * @return the location
	 */
	public String getLocation() {
		return location;
	}
	/**
	 * @param location the location to set
	 */
	public void setLocation(String location) {
		this.location = location;
	}
	/**
	 * @return the profile_image_url
	 */
	public String getProfile_image_url() {
		return profile_image_url;
	}
	/**
	 * @param profile_image_url the profile_image_url to set
	 */
	public void setProfile_image_url(String profile_image_url) {
		this.profile_image_url = profile_image_url;
	}
	/**
	 * @return the followers_count
	 */
	public double getFollowers_count() {
		return followers_count;
	}
	/**
	 * @param followers_count the followers_count to set
	 */
	public void setFollowers_count(double followers_count) {
		this.followers_count = followers_count;
	}
	/**
	 * @return the friends_count
	 */
	public double getFriends_count() {
		return friends_count;
	}
	/**
	 * @param friends_count the friends_count to set
	 */
	public void setFriends_count(double friends_count) {
		this.friends_count = friends_count;
	}
	/**
	 * @return the lang
	 */
	public String getLang() {
		return lang;
	}
	/**
	 * @param lang the lang to set
	 */
	public void setLang(String lang) {
		this.lang = lang;
	}
	/**
	 * @return the description
	 */
	public String getDescription() {
		return description;
	}
	/**
	 * @param description the description to set
	 */
	public void setDescription(String description) {
		this.description = description;
	}
	/**
	 * @return the time_zone
	 */
	public String getTime_zone() {
		return time_zone;
	}
	/**
	 * @param time_zone the time_zone to set
	 */
	public void setTime_zone(String time_zone) {
		this.time_zone = time_zone;
	}
	/**
	 * @return the statuses_count
	 */
	public String getStatuses_count() {
		return statuses_count;
	}
	/**
	 * @param statuses_count the statuses_count to set
	 */
	public void setStatuses_count(String statuses_count) {
		this.statuses_count = statuses_count;
	}
	/**
	 * @return the screen_name
	 */
	public String getScreen_name() {
		return screen_name;
	}
	/**
	 * @param screen_name the screen_name to set
	 */
	public void setScreen_name(String screen_name) {
		this.screen_name = screen_name;
	}
	
	
}
